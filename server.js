const express = require('express');
const cors = require('cors');
require('dotenv').config();
const { GoogleGenerativeAI } = require('@google/generative-ai');

const app = express();
const port = 3001;

app.use(cors());
app.use(express.json());

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({ model: "gemini-1.5-flash"});

app.post('/api/generate', async (req, res) => {
  try {
    const { step1, step2 } = req.body;

    const prompt = `
      Ты — элитный бренд-стратег и провокационный копирайтер, известный своей способностью создавать новые рыночные категории, а не просто конкурировать в существующих. Твой стиль — острый, лаконичный, смелый и невероятно точный. Твоя задача — проанализировать данные от эксперта и предложить 3 кардинально разных, но одинаково сильных варианта мета-идеи для его блога или продукта. Не предлагай очевидных или шаблонных идей. Каждая идея должна быть прорывной.

      Вот данные от эксперта:
      - Его главный "враг" в нише (что его раздражает): "${step1.annoyance}"
      - Его авторский метод решения проблемы: "${step1.correctMethod}"
      - Список того, что его клиенты ненавидят делать: ${step2.join(', ')}.

      Для каждого из 3-х вариантов мета-идеи, предоставь четкую структуру:

      ### Идея: [Название идеи]
      > [Цепляющий слоган или ключевая мысль в виде цитаты]
      
      **Концепция:** Детально опиши суть идеи, ее философию.
      **Отстройка от рынка:** Объясни, как эта идея конкретно противопоставляет эксперта его "врагу" и рынку в целом.
      **Оффер для аудитории:** Сформулируй предложение для клиентов, которое напрямую решает их "нелюбимые дела".

      Отформатируй свой ответ, используя Markdown для лучшей читаемости: используй заголовки (###), цитаты (>), списки (-) и выделение жирным шрифтом (**).
      ВАЖНО: После каждого из 3-х вариантов ставь разделитель в виде трех дефисов на новой строке: ---
    `;
    
    const result = await model.generateContent(prompt);
    const response = await result.response;
    const text = response.text();

    res.json({ ideas: text });
  } catch (error) {
    console.error(error);
    res.status(500).json({ error: 'Произошла ошибка при генерации идей' });
  }
});

app.listen(port, () => {
  console.log(`Сервер запущен на http://localhost:${port}`);
});